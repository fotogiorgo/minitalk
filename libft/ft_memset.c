#include <stdlib.h>

void	*ft_memset(void *str, int c, size_t n)
{
	size_t	i;

	i = 0;
	while (n > i)
	{
		*(char *)str = c;
		str++;
		i++;
	}
	str -= n;
	return (str);
}
